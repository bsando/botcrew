import Foundation

struct SpriteShapeSet: Codable, Equatable {
    var body: [[Int]]    // 8x10 — idle/reading
    var type: [[Int]]    // 8x10 — typing
    var shrug: [[Int]]   // 8x10 — waiting
    var error: [[Int]]   // 8x10 — error

    func shape(for status: AgentStatus) -> [[Int]] {
        switch status {
        case .typing: type
        case .waiting: shrug
        case .error: error
        case .reading, .idle: body
        }
    }
}

